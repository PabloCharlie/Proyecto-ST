def square(x, y):
    """Dibuja un cuadrado de color azul (pared) en las coordenadas (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for _ in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Calcula el índice del punto en la lista 'tiles'."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Retorna True si la posición es válida (no es una pared)."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Dibuja el mundo: paredes y alimento inicial."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Mueve a Pacman y a los fantasmas, maneja colisiones y puntos."""
    writer.undo()
    writer.write(state['score'])

    clear()

    # Movimiento de Pacman
    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    # Si pisa alimento, aumenta puntaje y marca como vacío (tile = 2)
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    # Dibujar Pacman
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # Movimiento de Fantasmas
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            # Si choca, elige una dirección aleatoria
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Detectar colisión con fantasmas (Fin del juego)
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    # Llamar a la función de nuevo (Velocidad del juego)
    ontimer(move, 100)


def change(x, y):
    """Cambia la dirección de Pacman si el giro es válido."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


# Configuración inicial de la ventana
setup(420, 420, 370, 0)
hideturtle()
tracer(False)
writer.goto(160, 160)
writer.color('white')
writer.write(state['score'])

# Captura de teclas
listen()
onkey(lambda: change(5, 0), 'Right')
onkey(lambda: change(-5, 0), 'Left')
onkey(lambda: change(0, 5), 'Up')
onkey(lambda: change(0, -5), 'Down')

# Iniciar juego
world()
move()
done()
